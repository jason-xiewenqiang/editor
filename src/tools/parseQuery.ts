interface FakeObj {
    [key: string]: any
}
/** https://www.baidu.com/s?ie=utf-8&f=8&rsv_bp=1&tn=baidu&wd=123
 * @param url 解析 url中的 query
 */
export default function parseQueryString(url: string) {
    if (!url || url.indexOf('?') === -1) return null;
    const queryString = url.substr(url.indexOf('?')+1); // 截取 ？后的 ie=utf-8&f=8&rsv_bp=1&tn=baidu&wd=123
    const kvArray = queryString.split('&')
    const query: FakeObj = {}
    kvArray.forEach(kv => {
        const [key, value] = kv.split('=');
        query[key as string] = value as string
    })
    return query
}

export function parseQueryStringByReg(url: string) {
    if (!url || url.indexOf('?') === -1) return null;
    const queryString = url.match(/(\S*\?)|(\S*)/g)[1]
    const kvArray = queryString.split('&')
    const query: FakeObj = {}
    kvArray.forEach(kv => {
        const [key, value] = kv.split('=');
        query[key as string] = value as string
    })
    return query
}

export function getQueryFromUrl (name: string, url:string) {
    if (!url || url.indexOf('?') === -1) return null;
    const regx = new RegExp("/(^|&|\\?)" + name + "=([^&]*)(&|$)")
    const res = url.match(regx);
    if (res) {
        return unescape(res[2])
    }
    return null
}